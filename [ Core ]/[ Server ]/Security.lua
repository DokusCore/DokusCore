CreateThread(function()
  local Exit = false
  if not (_Modules.SettingsMenu) then
    print(Error..'^6-------------------------------------------------------------------^0')
    print(Error..'^3The plugin ^1SettingsMenu ^3is a requirement for DokusCore to run!')
    print(Error..'^6-------------------------------------------------------------------^0')
    Exit = true
  end

  if not (_Modules.MultiCharacters) then
    print(Error..'^6-------------------------------------------------------------------^0')
    print(Error..'^3The plugin ^1MultiCharacters ^3is a requirement for DokusCore to run!')
    print(Error..'^6-------------------------------------------------------------------^0')
    Exit = true
  end

  if (Exit) then Wait(100) os.exit() end
end)
