function GetTime()
  local timestamp = os.time()
	local h = tonumber(os.date('%H', timestamp))
	local m = tonumber(os.date('%M', timestamp))
  local s = tonumber(os.date('%S', timestamp))
	return {h = h, m = m, s = s}
end
