RegisterCommand('help', function(s,a,r) SendConsoleLogo() SendHelpMenu() end)
--------------------------------------------------------------------------------
--------------------------------------------------------------------------------
RegisterCommand('DokusCore', function(s,a,r)
  if (Low(a[1]) == 'help') then SendConsoleLogo() SendHelpMenu() end
  if (Low(a[1]) == 'update') then TriggerEvent('DokusCore:Core:System:CheckUpdates') end
  if (Low(a[1]) == 'website') then
    if (Low(a[2]) == 'github') then SendConsoleLogo() SendGitHubLink() end
    if (Low(a[2]) == 'dokuscore') then SendConsoleLogo() SendDokusWebsite() end
  end
end)
