RegisterServerEvent('DokusCore:Core:DBIns:Blacklist')
AddEventHandler('DokusCore:Core:DBIns:Blacklist', function(source, args)
  if (Low(args[1]) == 'user') then
    if (Low(args[2]) == 'single') then
      DBIns(DB.Blacklist.Insert, {
      Steam = Steam, Reason = Reason, Admin = Admin,
      Until = NewDate, IP = IP, License = License,
      XBoxLive = XBoxLive, MLive = MLive
    })
    end
  end
end)
