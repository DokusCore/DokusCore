RegisterNetEvent('DokusCore:Core:Districts:Zones', function(Data)
  local Enable, All = (Data.Enable or false), (Data.All or false)
  local ID, Color = (Data.ID or ''), (Data.Color or false)
  local ColorHash = GetHash(_DC_Districts.Colors[Color])
  if (not (ColorHash)) then ColorHash = GetHash(_DC_Districts.Colors['Red1']) end

  if ((Enable) and (All)) then EnableAllDistrictsZones(ColorHash) end
  if ((Enable) and (not (All))) then EnableSingleDistrictsZones(ID, ColorHash) end
  if ((not (Enable)) and (All)) then DisableAllDistrictsZones() end
  if (not (Enable) and (not (All))) then DisableSingleDistrictsZones(ID) end
end)
