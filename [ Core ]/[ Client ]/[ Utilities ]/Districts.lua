local function EnableAllDistrictsZones(Color)
  for k,v in pairs(_DC_Districts.Zones) do
    if (not (v.Border)) then
      Citizen.InvokeNative(0x6786D7AFAC3162B3, v.Hash)
      Citizen.InvokeNative(0x563FCB6620523917, v.Hash, Color)
    end
  end
end
--------------------------------------------------------------------------------
--------------------------------------------------------------------------------
local function EnableSingleDistrictsZones(ID, Color)
  for k,v in pairs(_DC_Districts.Zones) do
    if (ID ~= '') then
      if (Low(v.ID) == (Low(ID))) then
        if (not (v.Border)) then
          Citizen.InvokeNative(0x6786D7AFAC3162B3, v.Hash)
          Citizen.InvokeNative(0x563FCB6620523917, v.Hash, Color)
        end
      end
    end
  end
end
